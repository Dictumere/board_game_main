def create_character(number):
    rand_skills = random.sample(SKILLS, k=3)
    runic_skill = []

    for skill in rand_skills:
        for key, value in LETTERS.items():
            skill = skill.replace(key, value)
        runic_skill.append(skill)

    name = {
        "first_name": FAKE.first_name(),
        "last_name": FAKE.last_name(),
        "job": FAKE.job(),
        "town": FAKE.city(),

        "strength": random.randint(3, 18),
        "agility": random.randint(3, 18),
        "endurance": random.randint(3, 18),
        "intelligence": random.randint(3, 18),
        "luck": random.randint(3, 18),

        "skill_1": runic_skill[0],
        "skill_2": runic_skill[1],
        "skill_3": runic_skill[2]
    }
    return name


def save_character(number, character):
    filename = f"result_{number}.svg"
    full_path = os.path.join("images", filename)
    fo.render_template("charsheet.svg", full_path, character)


def main():
    os.makedirs("images", exist_ok = True)

    for number in range(10):
        character = create_character(number + 1)
        save_character(number + 1, character)


if __name__ == "__main__":
    main()